#Day 3: Data type - int and str

age = 10
years = "10"

print(age + 5)          # Integer addition
print(years + "5")     # String concatenation

print(type(age))      # Output: <class 'int'>
print(type(years))    # Output: <class 'str'>

print(str(age) + "5")      # Convert int to str
print(int(years) + 5)      # Convert str to int
